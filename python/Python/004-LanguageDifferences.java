public class LanguageDifferences {
    public static void main(String[] args) {
        System.out.println(division(17,13));
        System.out.println(division(-8,2));
        System.out.println(division(15,4));
        System.out.println(division(5,10));
    }

    public static int division(int x, int y) {
        return x/y;
    }
}